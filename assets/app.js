const root = document.documentElement;
const languageSelect = document.querySelector("#languageSelect");
const themeToggle = document.querySelector("#themeToggle");

function applyLanguage(language) {
  const dict = translations[language] || translations.zh;
  root.lang = language === "ar" ? "ar" : language === "en" ? "en" : "zh-CN";
  root.dir = language === "ar" ? "rtl" : "ltr";
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    if (dict[key]) node.textContent = dict[key];
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
    const key = node.dataset.i18nPlaceholder;
    if (dict[key]) node.setAttribute("placeholder", dict[key]);
  });
  localStorage.setItem("scholardaily-language", language);
}

function applyTheme(theme) {
  root.dataset.theme = theme;
  const isDark = theme === "dark";
  themeToggle.setAttribute("aria-pressed", String(isDark));
  localStorage.setItem("scholardaily-theme", theme);
}

languageSelect.addEventListener("change", (event) => {
  applyLanguage(event.target.value);
});

themeToggle.addEventListener("click", () => {
  applyTheme(root.dataset.theme === "dark" ? "light" : "dark");
});

document.querySelectorAll(".save-button").forEach((button) => {
  button.addEventListener("click", () => {
    const saved = button.textContent === "★";
    button.textContent = saved ? "☆" : "★";
    button.classList.toggle("is-saved", !saved);
  });
});

const askDemoButton = document.querySelector("#askDemoButton");
const promptInput = document.querySelector("#copilotPrompt");
const chatStream = document.querySelector(".chat-stream");

askDemoButton?.addEventListener("click", () => {
  const language = localStorage.getItem("scholardaily-language") || "zh";
  const dict = translations[language] || translations.zh;
  const question = promptInput.value.trim() || dict.demoUserQuestion;

  chatStream.insertAdjacentHTML(
    "beforeend",
    `<article class="message user-message"><p>${escapeHtml(question)}</p></article>
    <article class="message ai-message">
      <div class="message-avatar">AI</div>
      <div>
        <p>${escapeHtml(dict.demoAiAnswer)}</p>
        <div class="citation-row" aria-label="Citations">
          <button type="button">[4] Benchmark Limits</button>
          <button type="button">[5] Data Contamination</button>
        </div>
      </div>
    </article>`
  );
  promptInput.value = "";
  chatStream.scrollTop = chatStream.scrollHeight;
});

function escapeHtml(value) {
  return value.replace(/[&<>"']/g, (char) => {
    const entities = {
      "&": "&amp;",
      "<": "&lt;",
      ">": "&gt;",
      '"': "&quot;",
      "'": "&#039;"
    };
    return entities[char];
  });
}

const preferredTheme = localStorage.getItem("scholardaily-theme") || "light";
const preferredLanguage = localStorage.getItem("scholardaily-language") || "zh";
languageSelect.value = preferredLanguage;
applyTheme(preferredTheme);
applyLanguage(preferredLanguage);
